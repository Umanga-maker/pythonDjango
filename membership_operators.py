# In operator

var = "TutorialsPoint"
a = "P"
b = "tor"
c = "in"
d = "To"
print (a, "in", var, ":", a in var)
print (b, "in", var, ":", b in var)
print (c, "in", var, ":", c in var)
print (d, "in", var, ":", d in var)


# Not in operator

var = "TutorialsPoint"
a = "P"
b = "tor"
c = "in"
d = "To"
print (a, " in", var, ":", a not in var)
print (b, " in", var, ":", b not in var)
print (c, " in", var, ":", c not in var)
print (d, " in", var, ":", d not in var)
